def setbonus(coreStats=None, resistances=None, statMods=None, grantsSkills=None) -> Dict[str, Any]:
    out: Dict[str, Any] = {}
    if coreStats: out["coreStats"] = coreStats
    if resistances: out["resistances"] = resistances
    if statMods: out["statMods"] = statMods
    if grantsSkills: out["grantsSkills"] = grantsSkills
    return out


def make_set(slug: str, name: str, member_keys: List[str], bonuses: Dict[str, Dict[str, Any]]) -> Dict[str, Any]:
    return {
        "id": f"sets/{slug}",
        "name": name,
        "items": [f"sets/{slug}/{k}" for k in member_keys],
        "bonuses": bonuses,
    }


