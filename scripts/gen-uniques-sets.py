def main() -> None:
    with open(UNIQUES_PATH, "r", encoding="utf-8") as fh:
        existing_uniques = json.load(fh)

    # Drop dup ids on the unique side.
    drop_unique_ids = {drop for _keep, drop in DUP_PAIRS}
    deduped_uniques = [u for u in existing_uniques if u["id"] not in drop_unique_ids]

    existing_ids = {u["id"] for u in deduped_uniques}

    # Append new uniques that don't collide with what we already have.
    new_count = 0
    for slug, name, slot, req, stats, flavor in NEW_UNIQUES:
        item_id = f"items/unique/{slug}"
        if item_id in existing_ids:
            continue
        base_id = base_for(slot, req)
        entry: Dict[str, Any] = {
            "id": item_id,
            "name": name,
            "baseId": base_id,
            "reqLevel": req,
            "stats": stats or {},
        }
        if flavor:
            entry["flavor"] = flavor
        deduped_uniques.append(entry)
        existing_ids.add(item_id)
        new_count += 1

    # Sort by reqLevel asc, then name asc.
    deduped_uniques.sort(key=lambda x: (x["reqLevel"], x["name"]))

    # Sets — drop duplicates entirely; replace with our authoritative SETS list.
    new_set_ids = {ss["id"] for ss in SETS}
    # Keep any existing set NOT in our new list (defensive — but we expect to overwrite all).
    with open(SETS_PATH, "r", encoding="utf-8") as fh:
        existing_sets = json.load(fh)

    drop_set_ids = {drop for _keep, drop in SET_DUP_PAIRS}
    # Discard duplicates AND any set we are replacing.
    kept_existing_sets = [
        s for s in existing_sets
        if s["id"] not in drop_set_ids and s["id"] not in new_set_ids
    ]
    final_sets = kept_existing_sets + SETS
    final_sets.sort(key=lambda x: x["name"])

    with open(UNIQUES_PATH, "w", encoding="utf-8", newline="\n") as fh:
        json.dump(deduped_uniques, fh, indent=2, ensure_ascii=False)
        fh.write("\n")

    with open(SETS_PATH, "w", encoding="utf-8", newline="\n") as fh:
        json.dump(final_sets, fh, indent=2, ensure_ascii=False)
        fh.write("\n")

    print(f"Uniques: existing kept={len(deduped_uniques)-new_count} new={new_count} total={len(deduped_uniques)}")
    print(f"Sets:    kept-existing={len(kept_existing_sets)} new/replaced={len(SETS)} total={len(final_sets)}")


if __name__ == "__main__":
    main()
