def base_for(slot: str, req: int) -> str:
    """Pick the highest base in `slot` whose reqLevel <= req."""
    if slot in SINGLE:
        return SINGLE[slot]
    tiers = BASE_BY_SLOT[slot]
    chosen = tiers[0][1]
    for r, bid in tiers:
        if r <= req:
            chosen = bid
        else:
            break
    return chosen


