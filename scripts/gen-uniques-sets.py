# Convenience builders
def cs(strength=None, dexterity=None, vitality=None, energy=None) -> Dict[str, int]:
    out = {}
    if strength is not None: out["strength"] = strength
    if dexterity is not None: out["dexterity"] = dexterity
    if vitality is not None: out["vitality"] = vitality
    if energy is not None: out["energy"] = energy
    return out

def res(fire=None, cold=None, lightning=None, poison=None, arcane=None, physical=None) -> Dict[str, float]:
    out = {}
    if fire is not None: out["fire"] = fire
    if cold is not None: out["cold"] = cold
    if lightning is not None: out["lightning"] = lightning
    if poison is not None: out["poison"] = poison
    if arcane is not None: out["arcane"] = arcane
    if physical is not None: out["physical"] = physical
    return out

def sm(**kw) -> Dict[str, float]:
    return {k: v for k, v in kw.items() if v is not None}

def dmg(min_, max_, **breakdown) -> Dict[str, Any]:
    return {"min": min_, "max": max_, "breakdown": breakdown}

def s(coreStats=None, resistances=None, statMods=None, grantsSkills=None, damageBonus=None) -> Dict[str, Any]:
    out: Dict[str, Any] = {}
    if coreStats: out["coreStats"] = coreStats
    if resistances: out["resistances"] = resistances
    if statMods: out["statMods"] = statMods
    if grantsSkills: out["grantsSkills"] = grantsSkills
    if damageBonus: out["damageBonus"] = damageBonus
    return out


